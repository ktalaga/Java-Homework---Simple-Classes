public class Calculator {

    public Calculator(){

    }

    public int add(int number_1, int number_2) {
        return number_1 + number_2;
    }


    public int subtract(int i, int i1) {
        return i - i1;
    }

    public int multiply(int i, int i1) {
        return i * i1;
    }

    public double divide(double v, double v1) {
        return v/v1;
    }
}
